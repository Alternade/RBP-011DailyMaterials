public class Boxing {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;
        //Boxing
        Integer a1 = a;
        Integer b1 = b;
        Integer c1 = c;
        //Unboxing
        int a2 = a1;
        int b2 = b1;
        int c2 = c1;


    }
}
